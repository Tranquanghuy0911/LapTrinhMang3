
public class Ngay {
private int ngay,thag,nam;

public Ngay(int ngay, int thag, int nam) {
	
	this.ngay = ngay;
	this.thag = thag;
	this.nam = nam;
}

public int getNgay() {
	return ngay;
}

public void setNgay(int ngay) {
	this.ngay = ngay;
}

public int getThag() {
	return thag;
}

public void setThag(int thag) {
	this.thag = thag;
}

public int getNam() {
	return nam;
}

public void setNam(int nam) {
	this.nam = nam;
}
}
